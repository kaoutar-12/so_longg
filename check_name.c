#include "so_long.h"

int ckeck_name(char *s)
{
	int i = ft_strlen(s);
	if (s[i - 1] == 'r' && s[i - 2] == 'e' && s[i - 3] == 'b' && s[i - 4] == '.' && i > 4)
		return (1);
	else
		return (0);
}